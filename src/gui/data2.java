    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // Mendapatkan fields dan values secara dinamis
        String[] fields = formHandler.getFieldNames();
        String[] values = formHandler.getFieldValues();

        // Cek field kosong
        if (formHandler.hasEmptyFields()) {
            JOptionPane.showMessageDialog(null, "Data tidak boleh kosong!");
            return;
        }

        // Cek duplicate key
        if (cruddb.DuplicateKey("tbl_data2", "kode_barang", values[0])) {
            JOptionPane.showMessageDialog(this, "Kode Barang sudah ada!");
            return;
        }

        cruddb.simpanDinamis("tbl_data2", fields, values);
        loaddata();
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        String[] fields = formHandler.getFieldNames();
        String[] values = formHandler.getFieldValues();

        if (jTextField1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Tidak ada data yang diubah!");
            return;
        }

        cruddb.UbahDinamis("tbl_data2", "kode_barang", jTextField1.getText(), fields, values);

        loaddata();
    }//GEN-LAST:event_btnUbahActionPerformed

