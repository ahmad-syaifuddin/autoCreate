    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try {
        int baris = jTable1.getSelectedRow();
        
        // Set text fields
        jTextField1.setText(jTable1.getValueAt(baris, 0).toString()); // id barang
        jTextField2.setText(jTable1.getValueAt(baris, 1).toString()); // nama barang
        jTextField3.setText(jTable1.getValueAt(baris, 3).toString()); // muatan
        jComboBox1.setSelectedItem(jTable1.getValueAt(baris, 5).toString());

        // Handle date for JCalendarComboBox
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String tanggal = jTable1.getValueAt(baris, 2).toString();
            Date date = sdf.parse(tanggal);
            jCalendarComboBox1.setDate(date);
        } catch (ParseException e) {
            System.err.println("Error parsing date: " + e.getMessage());
            JOptionPane.showMessageDialog(this, "Format tanggal tidak valid", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        // Handle value for JSpinner
        try {
            String jumlahStr = jTable1.getValueAt(baris, 4).toString();
            Integer jumlah = Integer.parseInt(jumlahStr);
            jSpinner1.setValue(jumlah);
        } catch (NumberFormatException e) {
            System.err.println("Error parsing number: " + e.getMessage());
            JOptionPane.showMessageDialog(this, "Format angka tidak valid", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    } catch (Exception e) {
        System.err.println("Error general: " + e.getMessage());
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat mengambil data", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jTable1MouseClicked

